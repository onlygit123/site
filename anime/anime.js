function updateURL(page) {
  const url = new URL(window.location);
  if (page === 1) {
    url.searchParams.delete("page");
  } else {
    url.searchParams.set("page", page);
  }
  history.pushState(null, null, url);
}

function goToPage(pageNumber) {
  const totalPages = Math.ceil(filteredAnimes.length / ITEMS_PER_PAGE);
  if (pageNumber < 1 || pageNumber > totalPages) return;

  currentPage = pageNumber;
  updateURL(currentPage);
  renderCurrentPage();
  renderPagination();

  window.scrollTo(0, 0);
}
function initSortButtons() {
  const btns = document.querySelectorAll(".sort-btn");
  btns.forEach((btn) => {
    if (btn.getAttribute("data-sort") === currentSort) {
      btn.classList.add("active", currentOrder);
    }
    btn.addEventListener("click", () => {
      const sortValue = btn.getAttribute("data-sort");
      if (!sortValue) return;
      if (currentSort === sortValue) {
        currentOrder = currentOrder === "desc" ? "asc" : "desc";
      } else {
        currentSort = sortValue;
        currentOrder = "desc";
        currentPage = 1;
        updateURL(1);
        updateDataAndRender();
      }
      btns.forEach((b) => {
        b.classList.remove("active", "asc", "desc");
        if (b.getAttribute("data-sort") === currentSort) {
          b.classList.add("active", currentOrder);
        }
      });
      updateDataAndRender();
    });
  });
}

function initThemeToggle() {
  const themeSwitch = document.getElementById("themeSwitch");
  if (!themeSwitch) return;

  const body = document.body;

  const savedTheme = localStorage.getItem("theme");
  if (savedTheme === "dark") {
    body.classList.add("dark-theme");
    themeSwitch.checked = true;
  }

  themeSwitch.addEventListener("change", function () {
    if (this.checked) {
      body.classList.add("dark-theme");
      localStorage.setItem("theme", "dark");
    } else {
      body.classList.remove("dark-theme");
      localStorage.setItem("theme", "light");
    }
  });
}

document.addEventListener("DOMContentLoaded", function () {
  const params = new URLSearchParams(window.location.search);
  let pageFromURL = parseInt(params.get("page"));
  if (!isNaN(pageFromURL) && pageFromURL >= 1) {
    currentPage = pageFromURL;
  } else {
    currentPage = 1;

    history.replaceState(null, null, window.location.pathname);
  }
  window.addEventListener("popstate", function () {
    const params = new URLSearchParams(window.location.search);
    let page = parseInt(params.get("page"));
    if (isNaN(page) || page < 1) page = 1;
    if (page !== currentPage) {
      currentPage = page;
      updateDataAndRender();
    }
  });

  initThemeToggle();

  initSortButtons();

  updateDataAndRender();
});
