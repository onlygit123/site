const ITEMS_PER_PAGE = 24;
let currentSort = "popularity";
let currentOrder = "desc";
let currentPage = 1;
let searchQuery = "";
let filteredAnimes = [...animeList];

function levenshteinDistance(a, b) {
  const matrix = [];
  for (let i = 0; i <= b.length; i++) matrix[i] = [i];
  for (let j = 0; j <= a.length; j++) matrix[0][j] = j;
  for (let i = 1; i <= b.length; i++) {
    for (let j = 1; j <= a.length; j++) {
      const cost = a[j - 1] === b[i - 1] ? 0 : 1;
      matrix[i][j] = Math.min(
        matrix[i - 1][j] + 1,
        matrix[i][j - 1] + 1,
        matrix[i - 1][j - 1] + cost,
      );
    }
  }
  return matrix[b.length][a.length];
}

function filterAnimesByQuery(query) {
  if (!query.trim()) return [...animeList];
  const lowerQuery = query.trim().toLowerCase();
  const exactMatches = [];
  const fuzzyMatches = [];
  for (let anime of animeList) {
    const titleLow = anime.title.toLowerCase();
    const engLow = (anime.english || "").toLowerCase();
    if (titleLow.includes(lowerQuery) || engLow.includes(lowerQuery)) {
      exactMatches.push(anime);
    } else {
      const maxDist = lowerQuery.length <= 3 ? 1 : 2;
      const titleDistance = levenshteinDistance(
        lowerQuery,
        titleLow.slice(0, lowerQuery.length + 2),
      );
      const engDistance = levenshteinDistance(
        lowerQuery,
        engLow.slice(0, lowerQuery.length + 2),
      );
      const minDist = Math.min(titleDistance, engDistance);
      if (minDist <= maxDist) {
        fuzzyMatches.push(anime);
      }
    }
  }
  return [...exactMatches, ...fuzzyMatches];
}

function sortAnimes(animes, sortType, order) {
  const arr = [...animes];
  if (sortType === "popularity") {
    return arr.sort((a, b) =>
      order === "desc"
        ? a.popularity - b.popularity
        : b.popularity - a.popularity,
    );
  }
  if (sortType === "score") {
    return arr.sort((a, b) =>
      order === "desc" ? b.score - a.score : a.score - b.score,
    );
  }
  if (sortType === "title") {
    return arr.sort((a, b) => {
      const comp = a.title.localeCompare(b.title, "ru");
      return order === "desc" ? comp : -comp;
    });
  }
  if (sortType === "year") {
    return arr.sort((a, b) =>
      order === "desc" ? b.year - a.year : a.year - b.year,
    );
  }
  return arr;
}

function updateDataAndRender() {
  const working = filterAnimesByQuery(searchQuery);
  filteredAnimes = sortAnimes(working, currentSort, currentOrder);

  renderCurrentPage();
  renderPagination();
}

function renderCurrentPage() {
  const startIdx = (currentPage - 1) * ITEMS_PER_PAGE;
  const endIdx = startIdx + ITEMS_PER_PAGE;
  const pageItems = filteredAnimes.slice(startIdx, endIdx);
  const gridContainer = document.getElementById("anime-grid-container");
  if (!pageItems.length) {
    gridContainer.innerHTML =
      '<div class="empty-state">Ничего не найдено. Попробуйте другое слово.</div>';
    return;
  }
  let cardsHtml = "";
  for (let anime of pageItems) {
    const firstLetter = anime.title.charAt(0).toUpperCase();
    const englishName = anime.english || "—";
    cardsHtml += `
                <div class="anime-card" data-id="${anime.id}">
                    <div class="card-poster">
                        <img src="${anime.image}" alt="${
                          anime.title
                        }" loading="lazy" 
                             onerror="this.style.display='none'; this.nextElementSibling.style.display='flex';">
                        <div class="poster-fallback" style="display: none;">${firstLetter}</div>
                    </div>
                    <div class="card-info">
                        <div class="card-title" title="${anime.title}">${
                          anime.title
                        }</div>
                        <div class="card-eng" title="${englishName}">${englishName}</div>
                        <div class="card-meta">
                            <span class="kind">${anime.kind}</span>
                            <span class="year">📅 ${anime.year}</span>
                            <span class="score">⭐ ${anime.score.toFixed(
                              1,
                            )}</span>
                        </div>
                    </div>
                </div>
            `;
  }
  gridContainer.innerHTML = cardsHtml;
  document.querySelectorAll(".anime-card").forEach((card) => {
    card.addEventListener("click", () => {
      const id = parseInt(card.getAttribute("data-id"));
      const anime = animeList.find((a) => a.id === id);
      if (anime) openDetailModal(anime);
    });
  });
}

function renderPagination() {
  const totalPages = Math.ceil(filteredAnimes.length / ITEMS_PER_PAGE);
  const paginationDiv = document.getElementById("pagination-controls");
  if (totalPages <= 1) {
    paginationDiv.innerHTML = "";
    return;
  }

  let pagHtml = `<button class="page-btn" id="prev-page-btn" ${currentPage === 1 ? "disabled" : ""}>Назад</button>`;
  let startPage = Math.max(1, currentPage - 3);
  let endPage = Math.min(totalPages, startPage + 6);
  if (endPage - startPage < 6) startPage = Math.max(1, endPage - 6);
  for (let i = startPage; i <= endPage; i++) {
    pagHtml += `<button class="page-btn ${i === currentPage ? "active-page" : ""}" data-page="${i}">${i}</button>`;
  }
  pagHtml += `<button class="page-btn" id="next-page-btn" ${currentPage === totalPages ? "disabled" : ""}>Вперёд</button>`;
  paginationDiv.innerHTML = pagHtml;

  document.querySelectorAll("[data-page]").forEach((btn) => {
    btn.addEventListener("click", (e) => {
      const newPage = parseInt(btn.getAttribute("data-page"));
      if (!isNaN(newPage)) goToPage(newPage);
    });
  });

  document.getElementById("prev-page-btn")?.addEventListener("click", () => {
    if (currentPage > 1) goToPage(currentPage - 1);
  });
  document.getElementById("next-page-btn")?.addEventListener("click", () => {
    if (currentPage < totalPages) goToPage(currentPage + 1);
  });
}

